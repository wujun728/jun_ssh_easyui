package model;

public class StoreType {
    private Integer storetypeid;

    private String storetypedesc;

    private String storetypename;

    private Integer storetypestate;

    public Integer getStoretypeid() {
        return storetypeid;
    }

    public void setStoretypeid(Integer storetypeid) {
        this.storetypeid = storetypeid;
    }

    public String getStoretypedesc() {
        return storetypedesc;
    }

    public void setStoretypedesc(String storetypedesc) {
        this.storetypedesc = storetypedesc;
    }

    public String getStoretypename() {
        return storetypename;
    }

    public void setStoretypename(String storetypename) {
        this.storetypename = storetypename;
    }

    public Integer getStoretypestate() {
        return storetypestate;
    }

    public void setStoretypestate(Integer storetypestate) {
        this.storetypestate = storetypestate;
    }
}